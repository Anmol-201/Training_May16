#include <stdio.h>
#define PI 3.14
int main()
{
    float radius;
    printf("Enter radius of circle: ");
    scanf("%f",&radius);
    printf("Circumference of Circle: %f\n",2*PI*radius);
    printf("Area of Circle: %f",PI*radius*radius);


    return 0;
}



